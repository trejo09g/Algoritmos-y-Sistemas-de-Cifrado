    private void jButton1EncriptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1EncriptarActionPerformed
        // Obtener el valor de desplazamiento ingresado por el usuario
        int shift = obtenerDesplazamiento();
        if (shift == -1) {
            return; // Si el valor no es válido, salir del método
        }
        // Limpiar el área de texto cifrado antes de agregar nuevo contenido
        jTextArea2TextoCifrado.setText("");

        char[] arrCar = jTextArea1TextoPlano.getText().toUpperCase().toCharArray();
        for (int i = 0; i < arrCar.length; i++) {
            if (Character.isLetter(arrCar[i])) {
                // Aplicar cifrado César con el desplazamiento ingresado
                int num = ((arrCar[i] - 65 + shift) % 26) + 65;
                jTextArea2TextoCifrado.setText(jTextArea2TextoCifrado.getText() + (char) num);
            } else {
                // Conservar caracteres no alfabéticos
                jTextArea2TextoCifrado.setText(jTextArea2TextoCifrado.getText() + arrCar[i]);
            }
        }
    }//GEN-LAST:event_jButton1EncriptarActionPerformed

