    private void jButton3DesencriptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3DesencriptarActionPerformed
// Obtener el valor de desplazamiento ingresado por el usuario
        int shift = obtenerDesplazamiento();
        if (shift == -1) {
            return; // Si el valor no es válido, salir del método
        }
        String input = jTextArea2TextoCifrado.getText().toUpperCase();
        StringBuilder decrypted = new StringBuilder();

        for (char c : input.toCharArray()) {
            if (Character.isLetter(c)) {
                // Aplicar desencriptado César con el desplazamiento ingresado
                int num = ((c - 'A' - shift + 26) % 26) + 'A';
                decrypted.append((char) num);
            } else {
                // Conservar caracteres no alfabéticos
                decrypted.append(c);
            }
        }

        jTextArea1TextoPlano.setText(decrypted.toString());
    }//GEN-LAST:event_jButton3DesencriptarActionPerformed

    private void jTextFieldDesplazamientoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextFieldDesplazamientoActionPerformed
        // Validar el desplazamiento cuando se presiona Enter en el campo
        obtenerDesplazamiento();
    }//GEN-LAST:event_jTextFieldDesplazamientoActionPerformed

    /**
     * Método para obtener y validar el valor de desplazamiento
     *
     * @return el valor de desplazamiento entre 1 y 25, o -1 si es inválido
     */
    private int obtenerDesplazamiento() {
        try {
            int shift = Integer.parseInt(jTextFieldDesplazamiento.getText());
            if (shift < 1 || shift > 25) {
                javax.swing.JOptionPane.showMessageDialog(this,
                        "El desplazamiento debe ser un número entre 1 y 25",
                        "Error",
                        javax.swing.JOptionPane.ERROR_MESSAGE);
                jTextFieldDesplazamiento.setText("3");
                return -1;
            }
            return shift;
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                    "Por favor ingrese un número válido entre 1 y 25",
                    "Error",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            jTextFieldDesplazamiento.setText("3");
            return -1;
        }

    }

