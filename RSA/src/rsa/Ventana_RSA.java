    // Método auxiliar para no amontonar todo
    private void abrirArchivo() {
        JFileChooser fileChooser = new JFileChooser();
        int seleccion = fileChooser.showOpenDialog(this);
        
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            File archivo = fileChooser.getSelectedFile();
            try {
                // Leer bytes y convertir a String
                String contenido = new String(Files.readAllBytes(archivo.toPath()));
                Entrada.setText(contenido);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error leyendo archivo: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

    private void btnTestLocalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTestLocalActionPerformed
        // TODO add your handling code here:
        try {
        // 1. VALIDACIONES PREVIAS
        if (misLlaves == null) {
            JOptionPane.showMessageDialog(this, "Error: Primero genera tus llaves (Botón Generar).");
            return;
        }
        String mensajeOriginal = Entrada.getText();
        if (mensajeOriginal.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Error: Escribe algo en el campo 'Entrada' para probar.");
            return;
        }

        String metodo = (String) Seleccionar_Metodo.getSelectedItem();
        
        // 2. CONFIGURAR EL CIPHER (El algoritmo matemático)
        Cipher cipher;
        if (metodo.equals("RSA")) {
            cipher = Cipher.getInstance("RSA/ECB/PKCS1Padding");
        } else if (metodo.equals("Elgamal")) {
            cipher = Cipher.getInstance("ElGamal/None/NoPadding", "BC");
        } else { // Curvas Elípticas (ECIES)
            cipher = Cipher.getInstance("ECIES", "BC");
        }

        // --- PASO A: ENCRIPTAR (Con TU llave pública) ---
        cipher.init(Cipher.ENCRYPT_MODE, misLlaves.getPublic());
        byte[] bytesEncriptados = cipher.doFinal(mensajeOriginal.getBytes());
        String textoEncriptadoBase64 = Base64.getEncoder().encodeToString(bytesEncriptados);

        // --- PASO B: DESENCRIPTAR (Con TU llave privada) ---
        cipher.init(Cipher.DECRYPT_MODE, misLlaves.getPrivate());
        byte[] bytesDesencriptados = cipher.doFinal(bytesEncriptados);
        String mensajeRecuperado = new String(bytesDesencriptados);

        // 3. MOSTRAR EVIDENCIA (Salida visual para la tarea)
        Salida.setText("--- REPORTE DE PRUEBA LOCAL ---\n" +
                       "Método: " + metodo + "\n" +
                       "1. Mensaje Original: " + mensajeOriginal + "\n" +
                       "2. Encriptado (Base64): " + textoEncriptadoBase64 + "\n" +
                       "3. Desencriptado: " + mensajeRecuperado + "\n" +
                       "--------------------------------\n" +
                       "ESTADO: " + (mensajeOriginal.equals(mensajeRecuperado) ? "¡ÉXITO!" : "FALLÓ"));
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Falló la prueba local: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnTestLocalActionPerformed

