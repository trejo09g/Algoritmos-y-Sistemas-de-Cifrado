    private void btnGenerarClavesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarClavesActionPerformed
        // TODO add your handling code here:
        try {
            String metodo = (String) Seleccionar_Metodo.getSelectedItem();
            KeyPairGenerator keyGen;
            
            // Configurar el generador según la selección
            switch (metodo) {
                case "RSA":
                    keyGen = KeyPairGenerator.getInstance("RSA");
                    keyGen.initialize(2048);
                    break;
                case "Elgamal":
                    // ElGamal requiere BouncyCastle (BC)
                    keyGen = KeyPairGenerator.getInstance("ElGamal", "BC");
                    keyGen.initialize(512); // O 1024
                    break;
                case "CurvasElipticas":
                    // ECC (EC) requiere BouncyCastle para facilitar ECIES
                    keyGen = KeyPairGenerator.getInstance("EC", "BC");
                    keyGen.initialize(256);
                    break;
                default:
                    return;
            }
            
            // Generar y guardar en variable global
            misLlaves = keyGen.generateKeyPair();
            
            // Mostrar en las cajas de texto (Codificado en Base64 para que sea legible)
            Mi_llave_publica.setText(Base64.getEncoder().encodeToString(misLlaves.getPublic().getEncoded()));
            Mi_llave_privada.setText(Base64.getEncoder().encodeToString(misLlaves.getPrivate().getEncoded()));
            
            JOptionPane.showMessageDialog(this, "Llaves generadas correctamente con " + metodo);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al generar llaves: " + e.getMessage());
        }
    }//GEN-LAST:event_btnGenerarClavesActionPerformed

    private void btnEncriptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEncriptarActionPerformed
        // TODO add your handling code here:
        try {
            String mensaje = Entrada.getText();
            if (mensaje.isEmpty()) return;

            String metodo = (String) Seleccionar_Metodo.getSelectedItem();
            PublicKey llaveDestino;

            // 1. DECIDIR QUÉ LLAVE USAR
            if (Llave_amigo.getText().trim().isEmpty()) {
                // Si no hay llave de amigo, usamos la nuestra (Local Loopback)
                if (misLlaves == null) {
                    JOptionPane.showMessageDialog(this, "Primero genera tus llaves.");
                    return;
                }
                llaveDestino = misLlaves.getPublic();
            } else {
                // Si hay llave de amigo, la convertimos de Texto -> Objeto PublicKey
                byte[] keyBytes = Base64.getDecoder().decode(Llave_amigo.getText().trim());
                KeyFactory kf;
                
                if (metodo.equals("RSA")) {
                    kf = KeyFactory.getInstance("RSA");
                } else if (metodo.equals("Elgamal")) {
                    kf = KeyFactory.getInstance("ElGamal", "BC");
                } else { // Curvas Elipticas
                    kf = KeyFactory.getInstance("EC", "BC");
                }
                
                llaveDestino = kf.generatePublic(new X509EncodedKeySpec(keyBytes));
            }

            // 2. CONFIGURAR EL CIPHER (Algoritmo de encriptación)
            Cipher cipher;
            if (metodo.equals("RSA")) {
                cipher = Cipher.getInstance("RSA/ECB/PKCS1Padding");
            } else if (metodo.equals("Elgamal")) {
                cipher = Cipher.getInstance("ElGamal/None/NoPadding", "BC");
            } else {
                cipher = Cipher.getInstance("ECIES", "BC");
            }

            // 3. ENCRIPTAR
            cipher.init(Cipher.ENCRYPT_MODE, llaveDestino);
            byte[] bytesEncriptados = cipher.doFinal(mensaje.getBytes());
            
            // Mostrar resultado en Base64
            Salida.setText(Base64.getEncoder().encodeToString(bytesEncriptados));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al encriptar: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnEncriptarActionPerformed

    private void btnDesencriptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDesencriptarActionPerformed
        // TODO add your handling code here:
        try {
            String mensajeCifradoStr = Entrada.getText(); // Ojo: Toma el texto cifrado de la Entrada
            if (mensajeCifradoStr.isEmpty() || misLlaves == null) {
                JOptionPane.showMessageDialog(this, "Necesitas un mensaje cifrado y tus llaves generadas.");
                return;
            }

            String metodo = (String) Seleccionar_Metodo.getSelectedItem();
            
            // 1. CONFIGURAR CIPHER
            Cipher cipher;
            if (metodo.equals("RSA")) {
                cipher = Cipher.getInstance("RSA/ECB/PKCS1Padding");
            } else if (metodo.equals("Elgamal")) {
                cipher = Cipher.getInstance("ElGamal/None/NoPadding", "BC");
            } else {
                cipher = Cipher.getInstance("ECIES", "BC");
            }

            // 2. DESENCRIPTAR CON MI LLAVE PRIVADA
            cipher.init(Cipher.DECRYPT_MODE, misLlaves.getPrivate());
            
            byte[] bytesEncriptados = Base64.getDecoder().decode(mensajeCifradoStr);
            byte[] bytesDesencriptados = cipher.doFinal(bytesEncriptados);
            
            Salida.setText(new String(bytesDesencriptados));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al desencriptar (¿Llave incorrecta?): " + e.getMessage());
        }
    }//GEN-LAST:event_btnDesencriptarActionPerformed

